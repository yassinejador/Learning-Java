public class WrapperClasses {
    public static void main(String[] args) {
        int num=1;
        Integer numWrapper=num; //Autoboxing

        Integer numWrapper1=1;
        int num1=numWrapper; //Unboxing

        /*
         * Wrapper classes in Java provide a way to work with primitive data types as objects
         * Autoboxing is a feature introduced in Java that automatically converts a primitive type to its corresponding wrapper class, and vice versa
         * Unboxing is the process of converting a wrapper object back into its corresponding primitive value
        */
    }
}
